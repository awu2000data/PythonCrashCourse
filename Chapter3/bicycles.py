#lists are individual elements separated by commas
#within square brackets []

bicycles = ["trek", "cannondale", "redline", "specialized"]
print(bicycles)
print("\n")

print("bicycles[0]: " + bicycles[0])
print("bicycles[0].title(): " + bicycles[0].title())
print("\n")

#Use index of [-1] to retrieve the last item
print("bicycles[-1]: " + bicycles[-1])
print("\n")

print("My first bicycle was a " + bicycles[0].title() + ".")
print("\n")